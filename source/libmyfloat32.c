#include <math.h>
#include <complex.h>

#include "libmyfloat32.h"

float fAdd(float a, float b)
{
    return a + b;
}

float fSubtract(float a, float b)
{
    return a - b;
}

float fMultiply(float a, float b)
{
    return a * b;
}

float fDivide(float a, float b)
{
    return a / b;
}
